# This script will run sphinx to create documentation for python sdk
#
# Use "generate_pydocs.sh" to update documentation in the docs directory.
#
# The exit-code of the script indicates success or a failure.

# Quit on any errors
set -e

# Create docs directory if it does not exist
mkdir -p target/docs/source

# Exclude autogenerated API message definition files that aren't part of SDK.
excluded_internal_clients=(apache_beam/internal/clients/)
python $(type -p sphinx-apidoc) -f -o target/docs/source apache_beam \
    "${excluded_internal_clients[@]}"

# Remove Cython modules from doc template; they won't load
sed -i -e '/.. automodule:: apache_beam.coders.stream/d' \
    target/docs/source/apache_beam.coders.rst

# Create the configuration and index files
cat > target/docs/source/conf.py <<'EOF'
extensions = [
    'sphinx.ext.autodoc',
    'sphinx.ext.napoleon',
    'sphinx.ext.viewcode',
]
master_doc = 'index'
html_theme = 'sphinxdoc'
project = 'Apache Beam'
EOF
cat > target/docs/source/index.rst <<'EOF'
Welcome to Apache Beam!
=======================

`Apache Beam <https://beam.apache.org>`_ is a unified model for defining both batch and streaming data-parallel processing pipelines. Beam provides a set of language-specific SDKs for constructing pipelines. These pipelines can be executed on distributed processing backends like `Apache Spark <http://spark.apache.org/>`_, `Apache Flink <http://flink.apache.org/>`_, and `Google Cloud Dataflow <http://cloud.google.com/dataflow>`_.

Please see the `Apache Beam website <https://beam.apache.org>`_ for more documention around usage and contributing.

Library Documentation
---------------------

* Complete library index: :ref:`genindex`
* Index of all modules: :ref:`modindex`
* Search all documentation: :ref:`search`

.. toctree::
   :maxdepth: 2
   :hidden:

   modules
EOF

# Build the documentation using sphinx
python $(type -p sphinx-build) -q target/docs/source target/docs/_build -c target/docs/source \
    -w "/tmp/sphinx-build.warnings.log"

# Message is useful only when this script is run locally.  In a remote
# test environment, this path will be removed when the test completes.
echo "Browse to file://$PWD/target/docs/_build/index.html"
